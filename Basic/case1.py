num1 = int(input('Digite um numero: '))
num2 = int(input('Digite outro numero: '))

adicao = num1 + num2
subtracao = num1 - num2
multiplicacao = num1 * num2
divisao = num1 / num2

print(f'A soma dos numeros {num1} + {num2} e igual a: ', adicao)
print(f'A subtacao dos numeros {num1} - {num2} e igual a: ', subtracao)
print(f'A multiplicacao dos numeros {num1} * {num2} e igual a: ', multiplicacao)
print(f'A divisao dos numeros {num1} / {num2} e igual a: ', divisao)